const I18n = (() => {
  let _lang = localStorage.getItem('sl_lang') || 'en';

  function t(key, vars) {
    const keys = key.split('.');
    let val = TRANSLATIONS[_lang];
    for (const k of keys) { if (val) val = val[k]; }
    if (!val) {
      // fallback to EN
      val = TRANSLATIONS['en'];
      for (const k of keys) { if (val) val = val[k]; }
    }
    if (!val) return key; // last resort
    if (vars) {
      return val.replace(/\{(\w+)\}/g, (_, name) => vars[name] !== undefined ? vars[name] : `{${name}}`);
    }
    return val;
  }

  function setLanguage(lang) {
    _lang = lang;
    localStorage.setItem('sl_lang', lang);
    document.documentElement.lang = lang;
    translateDOM();
  }

  function getLanguage() { return _lang; }

  function translateDOM() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const vars = el.getAttribute('data-i18n-vars');
      const parsed = vars ? JSON.parse(vars) : undefined;
      el.textContent = t(key, parsed);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
  }

  return { t, setLanguage, getLanguage, translateDOM };
})();

// Global shorthand — available to all other modules
const t = I18n.t.bind(I18n);
